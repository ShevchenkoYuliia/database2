use pcmouse;

-- Вибрати всі моделі мишок та їх ціни
SELECT Назва_моделі, Ціна FROM Компютерна_Мишка;

-- Підрахувати кількість товарів на складі
SELECT SUM(Кількість_на_складі) AS Загальна_кількість FROM Компютерна_Мишка;

-- Показати всі замовлення з їх деталями
SELECT Замовлення.Код_замовлення, Клієнт.Ім_я, Клієнт.Прізвище, Компютерна_Мишка.Назва_моделі, 
       Деталі_Замовлення.Кількість_товару, Деталі_Замовлення.Сума_за_позицію
FROM Замовлення
JOIN Клієнт ON Замовлення.Код_покупця = Клієнт.Код_покупця
JOIN Деталі_Замовлення ON Замовлення.Код_замовлення = Деталі_Замовлення.Код_замовлення
JOIN Компютерна_Мишка ON Деталі_Замовлення.Код_товару = Компютерна_Мишка.Код_товару;

-- Підрахувати загальну суму всіх замовлень
SELECT SUM(Загальна_сума_замовлення) AS Загальна_сума FROM Замовлення;

-- Показати постачальників, що постачають провідні миші
SELECT DISTINCT Постачальник.* FROM Постачальник
JOIN Компютерна_Мишка ON Постачальник.Код_постачальника = Компютерна_Мишка.Код_постачальника
WHERE Компютерна_Мишка.Тип = 'Провідна';
